for (const objectName of Object.keys(constants)) {
    label.object[objectName] = Object.keys(constants[objectName]).reduce(
        (acc, field) => ({...acc, [field]: `[${objectName}.${constants[objectName][field].NAME} unqueried]`}), {}
    );
}

const addObjectLabels = (objectName, objectFieldsInfo) => {
    label.object[objectName] = Object.keys(constants[objectName]).reduce((acc, field) => ({
        ...acc, [field]: objectFieldsInfo[constants[objectName][field].NAME] ? objectFieldsInfo[constants[objectName][field].NAME].label : null
    }), {});
}

